public class Operations {
    public static int Slojenie(int a, int b){
        int result=a+b;
        return result;
    }
    public static int Vichitanie (int a, int b){
        return a-b;
    }
    public static int Umnojenie (int a, int b){
        return a*b;
    }
    public static int Delenie (int a, int b){
        return a/b;
    }
}
