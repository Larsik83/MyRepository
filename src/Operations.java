public class Operations {
     static int Slojenie(int a, int b){
        int result=a+b;
        return result;
    }
     static int Vichitanie (int a, int b){
        return a-b;
    }
     static int Umnojenie (int a, int b){
        return a*b;
    }
     static int Delenie (int a, int b){
        return a/b;
    }
}
